    private void jBUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBUploadActionPerformed

        JFileChooser chooser = new JFileChooser(mainwin.getLastOpenPath());
        chooser.setDialogTitle("Vorlage hochladen");
        int result = chooser.showOpenDialog(this);
        if (result != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File selected = chooser.getSelectedFile();
        mainwin.setLastOpenPath(selected.getParent());

        new AutoMBox(EditBillingTemplate.class.getName()) {
            @Override
            public void do_stuff() throws Exception {
                byte[] data = Files.readAllBytes(selected.toPath());
                entity.odt_data.value = data;
                entity.file_name.loadFromString(selected.getName());
                if (entity.name.toString().trim().isEmpty()) {
                    String nameWithoutExt = selected.getName().replaceFirst("\\.[^.]+$", "");
                    entity.name.loadFromString(nameWithoutExt);
                    var_to_gui();
                }
                updateFileNameLabel();
                updateDownloadButton();
            }
        };

    }//GEN-LAST:event_jBUploadActionPerformed

    private void jBDownloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDownloadActionPerformed

        if (entity.odt_data.value == null || entity.odt_data.value.length == 0) {
            return;
        }

        JFileChooser chooser = new JFileChooser(mainwin.getLastOpenPath());
        chooser.setDialogTitle("Vorlage speichern");
        chooser.setSelectedFile(new File(entity.file_name.toString()));
        int result = chooser.showSaveDialog(this);
        if (result != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File target = chooser.getSelectedFile();
        mainwin.setLastOpenPath(target.getParent());

        new AutoMBox(EditBillingTemplate.class.getName()) {
            @Override
            public void do_stuff() throws Exception {
                try (FileOutputStream fos = new FileOutputStream(target)) {
                    fos.write(entity.odt_data.value);
                }
            }
        };

    }//GEN-LAST:event_jBDownloadActionPerformed

    private void jBSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSaveActionPerformed

        if (!check()) {
            return;
        }

        new AutoMBox(EditBillingTemplate.class.getName()) {
            @Override
            public void do_stuff() throws Exception {
                gui_to_var();
                Transaction trans = getTransaction();
                DefaultInsertOrUpdater.insertOrUpdateValuesWithPrimKey(trans, entity, entity.hist, root.getUserName());
                trans.commit();
                saved = true;
                close();
            }
        };

    }//GEN-LAST:event_jBSaveActionPerformed

