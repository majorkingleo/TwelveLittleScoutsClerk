    private boolean check() {
        if (entity.name.toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bitte einen Namen eingeben.", "Fehler", JOptionPane.ERROR_MESSAGE);
            jTName.requestFocus();
            return false;
        }
        return true;
    }

    public boolean pressedSave() {
        return saved;
    }

